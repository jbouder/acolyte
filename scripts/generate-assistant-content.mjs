import { writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';

const tools = [
  ['APIs', 'Test REST API endpoints.'],
  ['SSE', 'Test Server-Sent Event streams.'],
  ['WebSockets', 'Test WebSocket connections.'],
  ['Chat', 'Use OpenAI-compatible model providers.'],
  ['Web Stats', 'View website performance statistics.'],
  ['Website Analysis', 'Analyze website structure and metadata.'],
  ['Accessibility Checker', 'Check website accessibility compliance.'],
  ['Dependency Analysis', 'Analyze project dependencies.'],
  ['SBOM Report', 'Create a software bill of materials report.'],
  ['Markdown Preview', 'Render Markdown as you edit.'],
  ['Mermaid Preview', 'Render Mermaid diagrams.'],
  ['Swagger Viewer', 'View OpenAPI and Swagger specifications.'],
  ['Base64 Encoding', 'Encode and decode Base64 strings.'],
  ['JSON Formatter', 'Format, validate, and minify JSON.'],
  ['Regex Tester', 'Test regular expressions.'],
  ['Color Picker', 'Pick and convert colors.'],
  ['Image Tools', 'Crop, resize, convert images, and generate favicons.'],
  ['JWT Decoder', 'Decode and inspect JSON Web Tokens.'],
  ['Password Generator', 'Generate secure random passwords.'],
  ['Notepad', 'Write quick notes and scratch text.'],
];

const content = tools
  .map(([name, description]) => `- ${name}: ${description}`)
  .join('\n');
const output = `// This file is generated by scripts/generate-assistant-content.mjs.\nexport const assistantContent = [\n${content
  .split('\n')
  .map((line) => {
    if (line.includes("'")) {
      throw new Error('Assistant content cannot contain single quotes.');
    }
    return `  '${line}'`;
  })
  .join(',\n')},\n].join('\\n');\n`;

await writeFile(
  resolve(import.meta.dirname, '../lib/assistant-content.generated.ts'),
  output,
);
